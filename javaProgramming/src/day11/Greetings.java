public class Greetings {
	
	//1.No paramas   No return value
	
	void m1()
	{
		System.out.println("Hello...");
	}
	
	//2. No paramas  Return value
	
	String m2()
	{
		return("hello how are you");
	}
	
	//3 Take pram  no value
	void m3(String name)
	{
		System.out.println("hello  "+name);
	}
	
	//4.take param  return values
	String m4(String name) 
	{
		return("hello  "+name);
	}

}
