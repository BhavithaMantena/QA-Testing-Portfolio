package day3;

public class AssignmentOperator {
	public static void main(String[] args)
	{
		int a=10;
		a+=5; //a=a+5;
		System.out.println(a);
		
		int b=10;
		b-=5;  //b=b-5;
		System.out.println(b);
		
		int c=10;
		c*=2; //c=c*2;
		System.out.println(c);
		
		
		int d=10;
		d/=2; //d=d/2;
		System.out.println(d);
		
		int e=10;
		e%=2;  //e=e%2;
		System.out.println(e);
	}

}
